//bitwise operator...
public class bbit {
    public static void main(String args[]){
        //AND operator(&)
        System.out.println((5&6));
        //OR operator
        System.out.println((5|6));
        //XOR operator
        System.out.println((5^6));
        //1's compliment
        System.out.println((~5));
        //left shift
        System.out.println((5<<2));
        //right shift
        System.out.println(5>>2);
    }
    
}
